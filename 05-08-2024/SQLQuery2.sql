-- Selectors, Filtering

SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, COMM, DEPTNO
FROM EMP
WHERE YEAR(HIREDATE)='2011';


SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, COMM, DEPTNO
FROM EMP
WHERE YEAR(HIREDATE)='2011'
ORDER BY ENAME ;

SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, COMM, DEPTNO
FROM EMP
WHERE YEAR(HIREDATE)='2011'
ORDER BY SAL ;

SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, COMM, DEPTNO
FROM EMP
WHERE YEAR(HIREDATE)='2011'
ORDER BY SAL DESC;

SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, COMM, DEPTNO
FROM EMP
WHERE YEAR(HIREDATE)='2011'
