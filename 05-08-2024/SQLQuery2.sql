SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, ISNULL(COMM,0) AS COMM1,SAL + ISNULL(COMM,0) AS GROSSSAL
FROM EMP
ORDER BY COMM1 ;

SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, ISNULL(COMM,0) AS COMM1,SAL + ISNULL(COMM,0) AS GROSSSAL
FROM EMP
ORDER BY 6 DESC ;

SELECT EMPNO, ENAME, JOB, HIREDATE, SAL, ISNULL(COMM,0) AS COMM1,SAL + ISNULL(COMM,0) AS GROSSSAL,(ISNULL(COMM,0)/(SAL + ISNULL(COMM,0))) * 100 AS COMMPERC
FROM EMP
ORDER BY 6 DESC ;

